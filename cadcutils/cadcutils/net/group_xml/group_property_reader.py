from lxml import etree
from ..group import GroupProperty


class GroupPropertyReader(object):
    """

    """

    def read(self, xml_string):

        if xml_string is None or not xml_string:
            raise ValueError('xml_string is None or empty')

        root = etree.fromstring(xml_string)
        return self.get_group_property(root)

    def get_group_property(self, property_element):

        if property_element is None:
            raise ValueError('property element is None')

        if not property_element.tag == 'property':
            raise ValueError(
                'expected property element, found ' + property_element.tag)

        try:
            key = property_element.get('key')
        except KeyError:
            raise ValueError('GroupProperty missing required key attribute')

        try:
            type_attrib = property_element.get('type')
        except KeyError:
            raise ValueError('GroupProperty missing required type attribute')
        if type_attrib == GroupProperty.STRING_TYPE:
            value = property_element.text
        elif type_attrib == GroupProperty.INTEGER_TYPE:
            value = str(property_element.text)
        else:
            raise ValueError('Unsupported GroupProperty type ' + type_attrib)

        read_only_attrib = property_element.get('readOnly')
        if read_only_attrib is not None and read_only_attrib == 'true':
            read_only = True
        else:
            read_only = False

        return GroupProperty(key, value, read_only)
